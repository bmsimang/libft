#include "libft.h"

char	*ft_strcat(char *s1, const char *s2)
{
	char	*st;

	st = s1;
	while (*st != '\0')
		st++;
	while (*s2 != '\0')
	{
		*st = *s2;
		st++;
		s2++;
	}
	*st = '\0';
	return (s1);
}
